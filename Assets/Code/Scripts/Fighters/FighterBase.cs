using System.Collections;
using System.Collections.Generic;
using UnityEngine;

[CreateAssetMenu(fileName = "Enemy", menuName = "Enemy/Create new enemy")] //para hacer instncias de los atributos

public class FighterBase : ScriptableObject
{
    [SerializeField] string name; //serialize en vez de public porque public sale para poner algo en el script y serialize no

    [TextArea] // para hacer espacio para escribir más
    [SerializeField] string description;

    [SerializeField] Sprite rightSprite;
    [SerializeField] Sprite leftSprite;

    [SerializeField] FighterType type1;
    [SerializeField] FighterType type2;

    //Base stats
    [SerializeField] int maxHp;
    [SerializeField] int attack;
    [SerializeField] int defense;
    [SerializeField] int magicAttack;
    [SerializeField] int magickDefense;
    [SerializeField] int speed;
    [SerializeField] int expYield;

    [SerializeField] List<LearnableMove> learnableMoves;


    public int GetExpForLevel(int level)
    {
        return 4 * (level * level * level) / 5;
    }
    public string Name
    {
        get { return name; }
    }

    public string Description
    {
        get { return description; }
    }

    public Sprite RightSprite
    {
        get { return leftSprite; }
    }

    public Sprite LeftSprite
    {
        get { return rightSprite; }
    }

    public FighterType Type1
    {
        get { return type1; }
    }

    public FighterType Type2
    {
        get { return type2; }
    }

    public int MaxHp
    {
        get { return maxHp; }
    }

    public int Attack
    {
        get { return attack; }
    }

    public int Defense
    {
        get { return defense; }
    }

    public int MagickAttack
    {
        get { return magicAttack; }
    }

    public int MagickDefense
    {
        get { return magickDefense; }
    }

    public int Speed
    {
        get { return speed; }
    }

    public int ExpYield
    {
        get { return expYield; }
    }


    public List<LearnableMove> LearnableMoves
    {
        get { return learnableMoves; }
    }
}

[System.Serializable]
public class LearnableMove
{
    [SerializeField] MoveBase moveBase;
    [SerializeField] int level;

    public MoveBase Base
    {
        get { return moveBase; }
    }

    public int Level
    {
        get { return level; }
    }

}

